public class Main {

    public static void main(String[] args) {

        int num1 = 11;
        int num2 = 10;

        if (num1 == num2){
            System.out.println("The number is equal");
        }else if(num1 >= num2){
            System.out.println("The number 1 is bigger than number 2");
        }else if(num1 <= num2){
            System.out.println("The number 1 is smaller than number 2");
        }
    }
}
