public class Main {
    public static void main(String[] args) {

        int fallafel[][][] = new int [6][4][10];
        for(int i = 0; i < 6; i++){
            for(int j = 0; j < 4; j++){
                for(int k = 0; k < 10; k++){
                    System.out.print(fallafel[i][j][k]);
                }
                System.out.println();
            }
            System.out.println();
        }
    }
}
